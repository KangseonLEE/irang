export type TherapyTrackId = "healing" | "social";

export interface TherapyStat {
  label: string;
  value: string;
  year: string;
  source: string;
}

/** 인증·자격 정보 */
export interface TherapyCertification {
  hasCertification: boolean;
  /** 자격 보유 시 — 자격명·등급·법정 여부 등 */
  summary: string;
  /** 시험·교육 상세 (없으면 빈 배열) */
  details: { label: string; value: string }[];
  /** 신청 자격 (사회적 농업 등 자격증 없는 트랙용) */
  eligibility?: string[];
}

/** 수익 모델 */
export interface TherapyRevenue {
  /** 핵심 진입 경로·자금 흐름 정성 요약 */
  summary: string;
  /** 정부 지원 자금 — 키-값 리스트 */
  funding: { label: string; value: string; source: string }[];
  /** 공식 통계 미발표 경고 문구 */
  notice?: string;
}

/** 시장 현황 */
export interface TherapyMarket {
  /** 키 지표 1~5개 */
  indicators: TherapyStat[];
  /** 정성적 시장 흐름 한두 줄 */
  trend: string;
  notice?: string;
}

/** 우수 사례 */
export interface TherapyCase {
  name: string;
  location: string;
  /** 대상군·프로그램·인증 등 한 줄 요약 */
  oneLiner: string;
  /** 핵심 차별점 2~3건 */
  highlights: string[];
  recognition?: string;
}

/** FAQ */
export interface TherapyFAQ {
  q: string;
  a: string;
}

export interface TherapyTrack {
  id: TherapyTrackId;
  title: string;
  subtitle: string;
  definition: string;
  legalBasis: string;
  /** 진입 경로 step (기존 호환 유지) */
  entryPath: string[];
  /** 한눈 지표 (기존 호환 유지) */
  stats: TherapyStat[];
  certification: TherapyCertification;
  revenue: TherapyRevenue;
  market: TherapyMarket;
  cases: TherapyCase[];
  /** 핵심 지원사업 — 텍스트 요약 (programs.ts에 별도 row 없음) */
  supportPrograms: { title: string; detail: string; source: string }[];
  /** 체크리스트 — 진입 전 자가 점검 */
  checklist: string[];
  faq: TherapyFAQ[];
  officialUrl: string;
  officialUrlName: string;
  /** 추가 공식 링크 */
  extraLinks?: { name: string; url: string }[];
  /** YYYY-MM-DD. 출처 URL·수치 교차검증 마지막 일자 */
  verifiedAt: string;
}

